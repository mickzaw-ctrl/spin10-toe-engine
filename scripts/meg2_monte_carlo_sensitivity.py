# -----------------------------------------------------------------------------
# 1. BR(μ→eγ) master formula – SUSY-GUT SO(10) approximation
# -----------------------------------------------------------------------------

@jit
def br_muegamma(params: jnp.ndarray) -> jnp.ndarray:
    """
    Oblicza BR(μ→eγ) dla wektora parametrów.
    
    params[:, 0] = m_L  [TeV] – lepton doublet mass (common slepton mass)
    params[:, 1] = m_E  [TeV] – lepton singlet mass (common slepton mass)
    params[:, 2] = tanβ [1]   – Higgs VEV ratio
    params[:, 3] = θ12  [rad] – solar mixing angle (PMNS)
    params[:, 4] = m_N1 [GeV] – lightest right-handed neutrino mass
    params[:, 5] = δ_CP [rad] – Dirac CP phase
    
    Spin10 v13 constraint: γ = 0.2739 zamyka ε1 via ε1 ∝ f(γ, m_N1, m_N2, m_N3)
    → m_N1 nie jest wolne; wiąże się z M_GUT via RGE + LQG bridge.
    Dla symulacji: m_N1 ∈ [10⁹, 10¹²] GeV (typical leptogenesis scale).
    
    Formula (SUSY-GUT, chargino loop dominant, large tanβ):
    BR ≈ (α_em / 4π) · (tan²β / cos²β) · |A_L|² · (m_mu / M_SUSY)⁴
    
    gdzie A_L ∝ (m_N · U_eμ) · loop / (m_L², m_E²)
    
    Approximate: BR ≈ C × tan²β × sin²(2θ12) × (100 GeV / m_slepton)⁴
    z C ≈ 10⁻¹⁴ (normalization from SUSY-GUT literature).
    """
    m_L = params[:, 0] * 1e3  # TeV → GeV
    m_E = params[:, 1] * 1e3  # TeV → GeV
    tanβ = params[:, 2]
    θ12 = params[:, 3]
    m_N1 = params[:, 4]       # GeV
    δ_CP = params[:, 5]
    
    # Approximate SUSY-GUT amplitude
    # A ∝ m_mu · tanβ · (m_N / m_L²) · sin(2θ12) · e^{iδ}
    # In SO(10) with RGE running: enhanced by tanβ and PMNS mixing
    m_slepton = jnp.sqrt(m_L * m_E)  # geometric mean
    
    # Normalization factor: calibrated to SUSY-GUT literature
    # For m_slepton = 0.5 TeV, tanβ = 40: BR ~ 10⁻¹¹ (typical SUSY-GUT prediction)
    # C = 10⁻¹¹ / [(tanβ/10)² × (100/500)² × sin²(2×33°)] ≈ 1.4 × 10⁻¹¹
    C = 1.4e-11
    
    # Spin10 v13: γ = 0.2739 zamyka CP-asymmetry ε1 → leptogenesis scale
    # ε1 ∝ (m_N1 / M_GUT) · f(γ) → m_N1 typowo ~ 10¹⁰–10¹² GeV
    # leptogenesis enhancement factor (m_N1 dependence mild here)
    lepto_factor = jnp.log(m_N1 / 1e9) / 5.0  # 0.2–0.6 for 1e9–1e12 GeV
    
    # PMNS solar mixing dominant for μ→e
    pmns_factor = jnp.sin(2.0 * θ12) ** 2
    
    # tanβ enhancement (large tanβ regime: ∝ tan²β / cos²β ≈ tan⁴β for tanβ>>1)
    # but we use tan²β as more conservative
    tanβ_factor = (tanβ / 10.0) ** 2
    
    # mass suppression: m_slepton⁻² (loop suppression, dominant in SUSY-GUT)
    # mass_factor = (100.0 / m_slepton) ** 4  # too aggressive for 2-5 TeV
    mass_factor = (100.0 / m_slepton) ** 2  # loop-level suppression ~ m_slepton^-2
    
    BR = C * tanβ_factor * mass_factor * pmns_factor * (1.0 + 0.3 * lepto_factor)
    
    # CP-phase suppression/enhancement (Jarlskog-like, approximate)
    cp_factor = 1.0 + 0.2 * jnp.cos(δ_CP)
    BR = BR * cp_factor
    
    return jnp.clip(BR, 1e-20, 1.0)  # avoid underflow/overflow

# -----------------------------------------------------------------------------
# 2. Priorytetowe priory (Spin10 v13 constraints)
# -----------------------------------------------------------------------------

def spin10_v13_constraints(key, n_samples):
    """
    Generuje próbki z priorów zgodnych z Spin10 v13:
    - γ = 0.2739 (fixed) → leptogenesis scale m_N1 via RGE + LQG bridge
    - M_GUT = 1.03e16 GeV (fixed)
    - α_em = 1/137.036 (fixed)
    - m_slepton, tanβ, θ12, δ_CP from SUSY-GUT + PMNS priors
    """
    keys = random.split(key, 6)
    
    # m_L: SUSY-GUT common slepton mass, typically 0.5–5 TeV (HL-LHC reach)
    m_L = random.uniform(keys[0], (n_samples,), minval=0.3, maxval=5.0)
    
    # m_E: similar but can be slightly different (SO(10) GUT relation)
    m_E = m_L * (1.0 + 0.2 * random.normal(keys[1], (n_samples,)))  # m_E ≈ m_L ± 20%
    m_E = jnp.clip(m_E, 0.2, 10.0)
    
    # tanβ: 2–60 (SUSY-GUT range; large tanβ enhances BR(μ→eγ))
    tanβ = random.uniform(keys[2], (n_samples,), minval=2.0, maxval=60.0)
    
    # θ12: solar mixing angle, PDG 2024: 33.44° ± 0.77° (≈ 0.583 rad)
    θ12_deg = 33.44 + 0.77 * random.normal(keys[3], (n_samples,))
    θ12 = jnp.deg2rad(jnp.clip(θ12_deg, 31.0, 36.0))
    
    # m_N1: leptogenesis scale, Spin10 v13 via γ-bridge: typically 10⁹–10¹² GeV
    # log-uniform prior
    log_mN1 = random.uniform(keys[4], (n_samples,), minval=9.0, maxval=12.0)
    m_N1 = 10.0 ** log_mN1
    
    # δ_CP: Dirac CP phase, PDG 2024: 232° + 41° - 28° (≈ 4.05 rad)
    δ_CP_deg = 232.0 + random.normal(keys[5], (n_samples,)) * 35.0
    δ_CP = jnp.deg2rad(jnp.clip(δ_CP_deg, 180.0, 300.0))
    
    return jnp.stack([m_L, m_E, tanβ, θ12, m_N1, δ_CP], axis=1)

# -----------------------------------------------------------------------------
# 3. Monte Carlo: non-detection exclusion
# -----------------------------------------------------------------------------

def meg2_monte_carlo(key, n_samples=100_000):
    """
    Symulacja: ile % przestrzeni parametrów Spin10 v13 przetrwa
    non-detection MEG-II < 6×10⁻¹⁴?
    """
    params = spin10_v13_constraints(key, n_samples)
    
    # BR(μ→eγ) dla każdej próbki
    BR = br_muegamma(params)
    
    # MEG-II non-detection: BR < 6e-14
    survived = BR < MEG2_LIMIT
    excluded = ~survived
    
    # Statystyki przestrzeni parametrów
    results = {
        'n_samples': n_samples,
        'n_survived': int(jnp.sum(survived)),
        'n_excluded': int(jnp.sum(excluded)),
        'survival_rate': float(jnp.mean(survived)),
        'exclusion_rate': float(jnp.mean(excluded)),
        
        # Mediany przetrwałych (allowed region)
        'median_survived_mL': float(jnp.median(params[:, 0][survived])),
        'median_survived_mE': float(jnp.median(params[:, 1][survived])),
        'median_survived_tanb': float(jnp.median(params[:, 2][survived])),
        'median_survived_theta12': float(jnp.median(params[:, 3][survived])),
        'median_survived_mN1': float(jnp.median(params[:, 4][survived])),
        
    # Mediany wykluczonych (excluded region) – handle empty
    'median_excluded_mL': float(jnp.median(params[:, 0][excluded])) if jnp.sum(excluded) > 0 else float('nan'),
    'median_excluded_tanb': float(jnp.median(params[:, 2][excluded])) if jnp.sum(excluded) > 0 else float('nan'),
        
        # Min/Max przetrwałych (allowed bounds)
        'min_allowed_mL': float(jnp.min(params[:, 0][survived])),
        'max_allowed_mL': float(jnp.max(params[:, 0][survived])),
        'min_allowed_tanb': float(jnp.min(params[:, 2][survived])),
        'max_allowed_tanb': float(jnp.max(params[:, 2][survived])),
    }
    
    return results, params, BR, survived

# -----------------------------------------------------------------------------
# 4. Posteriorowe zagęszczenia (2D histograms)
# -----------------------------------------------------------------------------

def analyze_2d(params, BR, survived, bins=50):
    """
    Tworzy 2D histogramy: (m_slepton, tanβ) z exclusion contour.
    """
    m_s = jnp.sqrt(params[:, 0] * params[:, 1])  # geometric mean slepton mass
    tanb = params[:, 2]
    
    # Full prior
    H_full, xedges, yedges = jnp.histogram2d(
        m_s, tanb, bins=bins,
        range=[[0.2, 5.0], [2.0, 60.0]]
    )
    
    # Survived (allowed)
    H_surv, _, _ = jnp.histogram2d(
        m_s[survived], tanb[survived], bins=bins,
        range=[[0.2, 5.0], [2.0, 60.0]]
    )
    
    # Exclusion fraction per bin
    H_excl = jnp.where(H_full > 0, 1.0 - H_surv / H_full, 0.0)
    
    return {
        'xedges': xedges, 'yedges': yedges,
        'H_full': H_full, 'H_surv': H_surv, 'H_excl': H_excl,
        'xlabel': 'm_slepton [TeV] (sqrt(m_L * m_E))',
        'ylabel': 'tan β'
    }

